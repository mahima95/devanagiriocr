function [ out ] = printMatra( X )
%PRINTMATRA Summary of this function goes here
%   UTF-8 values of matras to the corresponding matras
        
    switch X
        case 0
            out='';
        case 1
            out='0947';
        case 2
            out='0948';
        case 3
            out='093F';
        case 4
            out='0940';
    end
            
            

end

